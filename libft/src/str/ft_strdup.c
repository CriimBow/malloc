#include "libft.h"

char	*ft_strdup(const char *src)
{
	char	*str;
	int		i;

	i = -1;
	str = (char *)ft_memalloc(sizeof(char) * (ft_strlen(src) + 1));
	if (str == NULL)
		return (NULL);
	while (src[++i])
		str[i] = src[i];
	return (str);
}
